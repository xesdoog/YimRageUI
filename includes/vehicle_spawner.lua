function GetVehicleNames()
    local vehNames = {}
    for _, v in ipairs(Vehicles_t) do
        local hash  = joaat(v)
        -- local make  = (VEHICLE.GET_MAKE_NAME_FROM_VEHICLE_MODEL(hash)):lower():gsub("^%l", string.upper)
        -- local model = vehicles.get_vehicle_display_name(hash)
        -- local displayName = string.format("%s %s", make, model)
        local displayName = vehicles.get_vehicle_display_name(hash)
        table.insert(vehNames, displayName)
    end
    return vehNames
end

---@param model integer
---@param inside_check boolean
function SpawnVeh(model, inside_check)
	script.run_in_fiber(function(sv)
		if not (INTERIOR.GET_INTERIOR_FROM_ENTITY(self.get_ped()) == 0) then
			gui.show_error("YimRageUI", "You can't spawn vehicles inside interiors.")
			return
		end
		if STREAMING.IS_MODEL_VALID(model) then
			while not STREAMING.HAS_MODEL_LOADED(model) do
				STREAMING.REQUEST_MODEL(model)
				coroutine.yield()
			end
			local myPos = ENTITY.GET_ENTITY_COORDS(self.get_ped(), true)
			local myFwdVec = ENTITY.GET_ENTITY_FORWARD_VECTOR(self.get_ped())
			local spawnPos = vec3:new(myPos.x + (myFwdVec.x * 5), myPos.y + (myFwdVec.y * 5), myPos.z)
			local veh = VEHICLE.CREATE_VEHICLE(
				model, spawnPos.x, spawnPos.y, spawnPos.z,
				ENTITY.GET_ENTITY_HEADING(self.get_ped()),
				true, false, false
			)
			sv:sleep(100)
			if ENTITY.DOES_ENTITY_EXIST(veh) then
				if inside_check then
					TASK.CLEAR_PED_TASKS_IMMEDIATELY(self.get_ped())
					PED.SET_PED_INTO_VEHICLE(self.get_ped(), veh, -1)
				end
				STREAMING.SET_MODEL_AS_NO_LONGER_NEEDED(model)
				ENTITY.SET_ENTITY_AS_NO_LONGER_NEEDED(veh)
			else
				gui.show_error("YimRageUI", "Failed to create vehicle!")
			end
		else
			gui.show_error("YimRageUI", "Invalid vehicle model!")
		end
	end)
end
